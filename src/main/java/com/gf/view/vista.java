    /**
     * Muestra los organizadores en el área de texto de la vista.
     */
    public void mostrarOrganizadores() {
        List<Organizador> organizadores = controller.obtenerTodosLosOrganizadores();
        StringBuilder sb = new StringBuilder();

        for (Organizador organizador : organizadores) {
            sb.append("Nº Organizador: ").append(organizador.getNOrganizador()).append("\n");
            sb.append("Paga Reserva: ").append(organizador.getPagaReserva()).append("\n");
            sb.append("Nombre: ").append(organizador.getNombre()).append("\n");
            sb.append("Teléfono: ").append(organizador.getTelefono()).append("\n");
            sb.append("Dirección: ").append(organizador.getDireccion()).append("\n");
            sb.append("Nombre Cumpleañero: ").append(organizador.getNombreCumpleanero()).append("\n");
            sb.append("------------------------\n");
        }

        textArea.setText(sb.toString());
    }


