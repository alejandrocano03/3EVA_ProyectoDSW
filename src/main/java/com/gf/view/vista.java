    /**
     * Inserta un nuevo organizador en la base de datos y muestra la lista actualizada.
     */
    public void insertarOrganizador() {
        JTextField nOrganizadorField = new JTextField();
        JTextField pagaReservaField = new JTextField();
        JTextField nombreField = new JTextField();
        JTextField telefonoField = new JTextField();
        JTextField direccionField = new JTextField();
        JTextField nombreCumpleaneroField = new JTextField();

        JPanel inputPanel = new JPanel(new GridLayout(6, 2));
        inputPanel.add(new JLabel("Nº Organizador:"));
        inputPanel.add(nOrganizadorField);
        inputPanel.add(new JLabel("Paga Reserva:"));
        inputPanel.add(pagaReservaField);
        inputPanel.add(new JLabel("Nombre:"));
        inputPanel.add(nombreField);
        inputPanel.add(new JLabel("Teléfono:"));
        inputPanel.add(telefonoField);
        inputPanel.add(new JLabel("Dirección:"));
        inputPanel.add(direccionField);
        inputPanel.add(new JLabel("Nombre Cumpleañero:"));
        inputPanel.add(nombreCumpleaneroField);

        int result = JOptionPane.showConfirmDialog(null, inputPanel, "Agregar Organizador", JOptionPane.OK_CANCEL_OPTION);
        if (result == JOptionPane.OK_OPTION) {
            try {
                int nOrganizador = Integer.parseInt(nOrganizadorField.getText());
                int pagaReserva = Integer.parseInt(pagaReservaField.getText());
                String nombre = nombreField.getText();
                int telefono = Integer.parseInt(telefonoField.getText());
                String direccion = direccionField.getText();
                String nombreCumpleanero = nombreCumpleaneroField.getText();

                Organizador organizador = new Organizador(nOrganizador, pagaReserva, nombre, telefono, direccion, nombreCumpleanero);
                controller.insertarOrganizador(organizador);
                mostrarOrganizadores();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "El número de organizador debe ser un valor entero.");
            }
        }
    }
    
    /**
     * Elimina un organizador de la base de datos y muestra la lista actualizada.
     */
    public void eliminarOrganizador() {
        String nOrganizadorStr = JOptionPane.showInputDialog(null, "Ingrese el número de organizador a eliminar:");
        if (nOrganizadorStr != null) {
            try {
                int nOrganizador = Integer.parseInt(nOrganizadorStr);
                controller.eliminarOrganizador(nOrganizador);
                mostrarOrganizadores();
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "El número de organizador debe ser un valor entero.");
            }
        }
    }

